tagMyMSG *g_pMSGAry = &g_MYMSGAry[0];
size_t g_nMSGCount = sizeof(g_MYMSGAry) / sizeof(g_MYMSGAry[0]);

BOOL GetIndex(UINT uMsg, size_t *pIndex)
{
    BOOL bRet = FALSE;
    for (size_t i = 0; i < g_nMSGCount; i++)
    {
        if (g_pMSGAry[i].dwType == uMsg)
        {   
            bRet = TRUE;
            *pIndex = i;

            //ignore some noisy
            if (WM_SYSKEYDOWN == uMsg
                || WM_ERASEBKGND == uMsg
                || WM_IME_NOTIFY == uMsg
                || WM_NCDESTROY == uMsg
                || WM_NCPAINT == uMsg)
            {
                bRet = FALSE;
            }
            
            break;
        }
    }
    
    return bRet;
}
